<template>
  <AlertWeb style="max-width: 400px; position: fixed; z-index: 9999" class="mt-15" />
  <v-app id="inspire" :class="mode">
    <NavBar :nav="drawer" v-if="router.currentRoute.value.name !== 'Login'" />
    <AppBar :drawer="drawer" @drawer="change" style="position: sticky; top: 0" v-if="loggedIn" />
    <AppbarHome v-if="!loggedIn" style="z-index: 999" />

    <NavbarHomeVue v-if="!loggedIn" />
    <v-main>
      <LoadProgres class="mt-15 loading" />

      <BtnBlobs v-if="loggedIn" />
      <router-view />
      <router-view name="adri"></router-view>
    </v-main>
    <footer v-if="loggedIn"><FooterView /></footer>
    <FooterHomeVue v-if="!loggedIn && router.currentRoute.value.name !== 'Login'" />
  </v-app>
</template>

<script setup>
import AlertWeb from '@/components/AlertWeb.vue';
import BtnBlobs from '@/components/BtnBlobs.vue';
import AppBar from '@/components/AppBar.vue';
import NavBar from '@/components/NavBar.vue';
import NavbarHomeVue from '@/components/NavbarHome.vue';
import LoadProgres from '@/components/LoadProgres.vue';

import FooterView from '@/components/FooterView.vue';
import AppbarHome from '@/components/AppbarHome.vue';
import FooterHomeVue from '@/components/FooterHome.vue';

import { computed, ref } from 'vue';
import { useStore } from 'vuex';
import { useRouter } from 'vue-router';

const router = useRouter();
const store = useStore();

const mode = computed(() => {
  if (store.state.mode) {
    return 'light-mode';
  } else {
    return 'dark-mode';
  }
});
const drawer = ref(null);
const loggedIn = computed(() => store.state.auth.loggedIn);

const change = (value) => {
  drawer.value = value;
};
</script>

